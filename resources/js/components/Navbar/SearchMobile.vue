<template>
  <!-- Search -->
  <div class="relative w-auto mx-4 my-4">
    <div
      v-if="isSearchOnFocus == false && search.length == 0"
      class="absolute flex flex-row items-center mt-4 ml-4"
    >
      <svg
        xmlns="http://www.w3.org/2000/svg"
        class="h-6 w-6 mr-2 text-gray-400"
        fill="none"
        viewBox="0 0 24 24"
        stroke="currentColor"
      >
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          stroke-width="2"
          d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"
        />
      </svg>
      <p class="text-gray-200 text-sm font-semibold">Search</p>
    </div>
    <input
      v-model="search"
      @change="syncSearchInput"
      @focus="toggleSearch"
      @blur="toggleSearch"
      type="text"
      class="text-gray-300 focus:outline-none rounded-md bg-secondary px-6 py-4 w-full"
    />
  </div>
</template>

<script>
import { mapGetters, mapActions } from 'vuex'
export default {
   data () {
    return {
      search: ''
    }
  },
  computed: {
    ...mapGetters({
      isSearchIdle: 'navbar/isSearchIdle',
      isSearchOnFocus: 'navbar/isSearchOnFocus'
    })
  },
  methods: {
    ...mapActions({
      toggleSearch: 'navbar/toggleSearch',
      setSearch: 'navbar/setSearch'
    }),
    syncSearchInput () {
      this.setSearch(this.search);
    }
  }
};
</script>

